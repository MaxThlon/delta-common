package delta.common.utils.traces;

/**
 * Registry for all loggers names.
 * @author DAM
 */
public class LoggingConstants
{
  /**
   * Separator to use between logger name components.
   */
  public static final String SEPARATOR=".";

  /**
   * Code name for the root logger.
   */
  public static final String ROOT_LOGGER_NAME="@root@";

  /**
   * Name of the standard output.
   */
  public static final String CONSOLE="console";

  /**
   * Name of the "logging" module.
   */
  public static final String MODULE_NAME="logging";
}
